// NewsPrep — Config

// Auth helpers — localStorage only
window.getUser   = () => JSON.parse(localStorage.getItem("ca_user") || "null");
window.setUser   = (u) => localStorage.setItem("ca_user", JSON.stringify(u));
window.clearUser = () => localStorage.removeItem("ca_user");
window.isAdmin   = () => { const u = getUser(); return !!(u && u.role === "admin"); };
window.isLogged  = () => !!getUser();

window.requireLogin = () => { if (!isLogged()) { window.location.href = "login.html"; return false; } return true; };
window.requireAdmin = () => { if (!isAdmin()) { window.location.href = "index.html"; return false; } return true; };

// Fetch articles from static JSON (generated by GitHub Actions pipeline)
window.fetchArticles = async () => {
  const res = await fetch('data/articles.json?t=' + Date.now());
  if (!res.ok) throw new Error("Failed to load articles.json");
  const json = await res.json();
  return json.articles || [];
};

// Quiz localStorage helpers
window.getQuizHistory = () => { try { return JSON.parse(localStorage.getItem("ca_quiz") || "[]"); } catch { return []; } };
window.saveQuizResult = (r) => { const h = getQuizHistory(); h.unshift(r); localStorage.setItem("ca_quiz", JSON.stringify(h.slice(0, 50))); };
window.getBookmarks   = () => { try { return JSON.parse(localStorage.getItem("ca_bookmarks") || "{}"); } catch { return {}; } };
window.saveBookmarks  = (b) => localStorage.setItem("ca_bookmarks", JSON.stringify(b));
window.getReadHistory = () => { try { return JSON.parse(localStorage.getItem("ca_read") || "[]"); } catch { return []; } };
window.markRead       = (id) => { const h = getReadHistory(); if (!h.includes(id)) { h.unshift(id); localStorage.setItem("ca_read", JSON.stringify(h.slice(0, 200))); } };

// Category-wise read tracking
window.getCatReads = () => { try { return JSON.parse(localStorage.getItem("ca_cat_reads") || "{}"); } catch { return {}; } };
window.markCatRead = (category) => {
  if(!category) return;
  const today = new Date().toISOString().slice(0,10);
  const data = getCatReads();
  if(!data[today]) data[today] = {};
  data[today][category] = (data[today][category] || 0) + 1;
  // Keep only last 30 days
  const keys = Object.keys(data).sort();
  if(keys.length > 30) keys.slice(0, keys.length - 30).forEach(k => delete data[k]);
  localStorage.setItem("ca_cat_reads", JSON.stringify(data));
};

window.trackVisit = () => {
  const u = getUser();
  const visits = JSON.parse(localStorage.getItem("ca_visits") || "[]");
  visits.push({ time: new Date().toISOString(), page: location.pathname, user: u?.email || "guest" });
  localStorage.setItem("ca_visits", JSON.stringify(visits.slice(-500)));
};

// ── Theme (dark / light mode) ──────────────────────────────────────────────
window.getTheme    = () => localStorage.getItem("ca_theme") || "dark";
window.setTheme    = (t) => { localStorage.setItem("ca_theme", t); applyTheme(t); };
window.toggleTheme = () => setTheme(getTheme() === "dark" ? "light" : "dark");

window.applyTheme = (theme) => {
  const root = document.documentElement;
  if (theme === "light") {
    root.style.setProperty("--bg",      "#f5f6fa");
    root.style.setProperty("--surface", "#ffffff");
    root.style.setProperty("--border",  "#dde1e7");
    root.style.setProperty("--text",    "#1a1d23");
    root.style.setProperty("--muted",   "#6b7280");
    root.style.setProperty("--card",    "#ffffff");
    root.style.setProperty("--accent",  "#d4900a");
    root.style.setProperty("--green",   "#16a34a");
    root.style.setProperty("--blue",    "#2563eb");
    root.style.setProperty("--red",     "#dc2626");
  } else {
    root.style.setProperty("--bg",      "#0d1117");
    root.style.setProperty("--surface", "#161b22");
    root.style.setProperty("--border",  "#30363d");
    root.style.setProperty("--text",    "#e6edf3");
    root.style.setProperty("--muted",   "#7d8590");
    root.style.setProperty("--card",    "#1c2128");
    root.style.setProperty("--accent",  "#f4b942");
    root.style.setProperty("--green",   "#3fb950");
    root.style.setProperty("--blue",    "#58a6ff");
    root.style.setProperty("--red",     "#e05c5c");
  }
  // Update toggle button label if present
  const btn = document.getElementById("themeToggleBtn");
  if (btn) btn.textContent = theme === "light" ? "🌙 Dark Mode" : "☀️ Light Mode";
};

// Auto-apply saved theme on every page load
(function() { applyTheme(getTheme()); })();
